// 실습하기 1-1 To create a practice table
// DEPARTMENT TABLE
CREATE TABLE DEPT (
    DEPTNO  NUMBER(2) CONSTRAINT PK_DEPT PRIMARY KEY,
    DNAME   VARCHAR2(14),
    LOC     VARCHAR2(13)
);

// EMPLOYEES TABLE
CREATE TABLE EMP (
    EMPNO   NUMBER(4) CONSTRAINT PK_EMP PRIMARY KEY,
    ENAME   VARCHAR2(10),
    JOB     VARCHAR2(9),
    MGR     NUMBER(4),
    HIREDATE DATE,
    SAL     NUMBER(7,2),
    COMM    NUMBER(7,2),
    DEPTNO  NUMBER(2) CONSTRAINT FK_DEPTNO REFERENCES DEPT 
);

// SALARY TABLE 
CREATE TABLE SALGRADE (
  GRADE NUMBER,
  LOSAL NUMBER,
  HISAL NUMBER
  );
  
//BONUS TABLE 
CREATE TABLE BONUS (
    ENAME VARCHAR2(10),
    JOB   VARCHAR2(9),
    SAL   NUMBER,
    COMM  NUMBER
);

